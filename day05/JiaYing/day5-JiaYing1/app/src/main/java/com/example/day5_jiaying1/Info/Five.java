package com.example.day5_jiaying1.Info;

public class Five {
    private int fiveImgid;
    private String fivetitles;
    private String fivedesc;

    public Five(int fiveImgid, String fivetitles, String fivedesc) {
        this.fiveImgid = fiveImgid;
        this.fivetitles = fivetitles;
        this.fivedesc = fivedesc;
    }

    public int getFiveImgid() {
        return fiveImgid;
    }

    public void setFiveImgid(int fiveImgid) {
        this.fiveImgid = fiveImgid;
    }

    public String getFivetitles() {
        return fivetitles;
    }

    public void setFivetitles(String fivetitles) {
        this.fivetitles = fivetitles;
    }

    public String getFivedesc() {
        return fivedesc;
    }

    public void setFivedesc(String fivedesc) {
        this.fivedesc = fivedesc;
    }
}
