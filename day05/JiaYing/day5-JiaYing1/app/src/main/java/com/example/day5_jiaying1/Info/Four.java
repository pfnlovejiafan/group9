package com.example.day5_jiaying1.Info;

public class Four {
    private int fourImgID;
    private String fourtitle;

    public Four(int fourImgID, String fourtitle) {
        this.fourImgID = fourImgID;
        this.fourtitle = fourtitle;
    }

    public Four() {
    }

    public int getFourImgID() {
        return fourImgID;
    }

    public void setFourImgID(int fourImgID) {
        this.fourImgID = fourImgID;
    }

    public String getFourtitle() {
        return fourtitle;
    }

    public void setFourtitle(String fourtitle) {
        this.fourtitle = fourtitle;
    }
}
